# Integer
integer_var = 42
print("Integer:", integer_var)
print("Integer + 10:", integer_var + 10)

# Float
float_var = 3.14
print("Float:", float_var)
print("Float * 2:", float_var * 2)

# String
string_var = "Hello, Python!"
print("String:", string_var)
print("String concatenated:", string_var + " Welcome!")

# Boolean
bool_var = True
print("Boolean:", bool_var)
print("Negation:", not bool_var)

# List
list_var = [1, 2, 3, 4, 5]
print("List:", list_var)
print("List length:", len(list_var))

# Tuple
tuple_var = (10, 20, 30)
print("Tuple:", tuple_var)
print("Accessing tuple element:", tuple_var[1])

# Set
set_var = {1, 2, 2, 3, 4, 4, 5}
print("Set:", set_var)
print("Set union:", set_var | {5, 6, 7})

# Dictionary
dict_var = {'a': 1, 'b': 2, 'c': 3}
print("Dictionary:", dict_var)
print("Value associated with 'b':", dict_var['b'])

# NoneType
none_var = None
print("NoneType:", none_var)

# Complex
complex_var = 2 + 3j
print("Complex:", complex_var)
print("Real part:", complex_var.real)
print("Imaginary part:", complex_var.imag)
