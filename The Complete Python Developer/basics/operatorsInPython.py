number_1 = 10
number_2 = 2

# Arithmetic Operators
print("\n# Arithmetic Operators\n")
print("Num1 + Num2 ", number_1 + number_2)
print("Num1 - Num2 ", number_1 - number_2)
print("Num1 * Num2 ", number_1 * number_2)
print("Num1 / Num2 ", number_1 / number_2)
print("Num1 // Num2 ", number_1 // number_2)
print("Num1 ** Num2 ", number_1 ** number_2)
print("Num1 % Num2 ", number_1 % number_2)

# Assignment Operators
print("\n#Assignment Operators\n")
a = 4
a += 2 # shorthand  a = a + 2
print(a)

# Comparison Operators
print("\n# Comparison Operators\n")

x = 8
y = 3
z = 8

print(x > y)
print(x < y)
print(x != y)
print(z == x)

# Logical Operators
print("\n# Logical Operators\n")
print(x == z and x < y)
print(x == z or x < y)
print (not(False))
print(not(True))

